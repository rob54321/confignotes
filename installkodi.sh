# how to install latest kodi from repository

apt-get install software-properties-common
add-apt-repository ppa:team-xbmc/ppa
apt-get update
apt-get install kodi

# to install exodus for kodi 17.6

open kodi then file manager.
add source then add http://archive.org/download/repository.xvbmc
set the name to xvbmc or something
back to main menu
click add-ons
click package installer on top left
install from zip file
click xvbmc
click repository/xvbmc-4.xxx.zip
wait till it says installed
click install from repository
click xvbmc repository
click add-on repository
select tknorris release repository
click it then click install button
wait for tknorris release repository addons installed
go back click install from repository then click tknorris release
video addons -> exodus -> install button

